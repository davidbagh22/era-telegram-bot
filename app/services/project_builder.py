def question_text(index: int) -> str:
    question = PROJECT_QUESTIONS[index]
    return (
        f"{question.block}\n"
        f"Шаг {index + 1} из {len(PROJECT_QUESTIONS)}\n\n"
        f"{question.title}\n\n{question.prompt}"
    )


