@router.callback_query(ProjectStates.answer, F.data.startswith("project:hint:"))
async def detailed_project_hint(call: CallbackQuery, state: FSMContext) -> None:
    await call.answer()
    index = int(call.data.rsplit(":", 1)[-1])
    data = await state.get_data()
    if index != int(data.get("question_index", -1)):
        await call.message.answer("Эта подсказка относится к предыдущему шагу")
        return
    prompt = DETAILED_HINTS.get(index)
    if not prompt:
        prompt = "Ответьте конкретно: что делаем, для кого, зачем, какой результат и как это усилит ЭРА. Уберите общие слова и добавьте действия."
    await call.message.answer("Подсказка для усиления ответа:\n\n" + prompt)
