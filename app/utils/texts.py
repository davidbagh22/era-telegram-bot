def points_text(total: int, history: Iterable[tuple[int, str]]) -> str:
    lines = [f"{value:+d} — {reason}" for value, reason in history]
    tail = "\n".join(lines) if lines else "История пока пустая."
    return f"""Ваши баллы: {total}

Баллы показывают Вашу активность и вклад в ЭРА.

Они начисляются за участие, помощь, задачи, проекты, волонтёрство, медиа, организацию и другие реальные действия.

Последние операции:
{tail}"""


def rating_text(
    rows: Iterable[tuple[str, int]], current_place: int | str, points: int
) -> str:
    top = "\n".join(
        f"{index}. {name} — {score}" for index, (name, score) in enumerate(rows, 1)
    )
    return f"""Рейтинг ЭРА

Ваше место: {current_place}
Ваши баллы: {points}

Топ участников:
{top or "Рейтинг пока формируется."}

Рейтинг — не про соревнование ради цифр.

Он показывает людей, которые двигаются, помогают и берут ответственность."""


